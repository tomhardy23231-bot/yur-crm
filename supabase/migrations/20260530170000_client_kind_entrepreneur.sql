-- Юр CRM — Задача 7: тип клиента «ФОП» (физлицо-предприниматель).
--
-- Для Украины ФОП (фізична особа-підприємець) — отдельный, частый статус клиента
-- наряду с физлицом и юрлицом. Добавляем значение в enum public.client_kind.
--
-- Примечание про транзакции: ALTER TYPE ... ADD VALUE в PostgreSQL 12+ допустим
-- внутри транзакции (Supabase CLI оборачивает каждую миграцию в транзакцию), при
-- условии что новое значение НЕ используется в той же транзакции. Здесь мы только
-- добавляем значение — никаких вставок с ним в этом файле, поэтому безопасно.
-- IF NOT EXISTS делает миграцию идемпотентной (повторный прогон не упадёт).

alter type public.client_kind add value if not exists 'entrepreneur';
