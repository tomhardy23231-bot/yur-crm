-- Юр CRM — Идемпотентность платежа (Задача 2, defense-in-depth).
--
-- Проблема: на клиенте двойной клик защищён (кнопка disabled), но программный
--   мульти-сабмит (3 клика за <16 мс) создал 3 одинаковых платежа — серверной
--   идемпотентности не было. Риск низкий (человеку так не кликнуть), но это
--   деньги, поэтому закрываем на уровне БД.
--
-- Модель: форма при КАЖДОЙ отправке генерирует уникальный ключ (idempotency_key).
--   Частичный уникальный индекс не даёт записать второй платёж с тем же ключом —
--   атомарно, без гонок (в отличие от «проверить-потом-вставить»). Server action
--   ловит нарушение уникальности (SQLSTATE 23505) и ТИХО возвращает успех: первый
--   платёж прошёл, дубль просто не создаётся, ошибку пользователю не показываем.
--
-- Колонка nullable: старые/прочие пути вставки (сид, импорт) без ключа продолжают
--   работать; индекс частичный (where ... is not null) — несколько NULL допустимы.

alter table public.payments
  add column idempotency_key uuid;

comment on column public.payments.idempotency_key is
  'Ключ идемпотентности отправки формы (Задача 2). Уникален среди не-NULL → '
  'повторная вставка того же платежа (мульти-сабмит) отвергается на уровне БД.';

create unique index payments_idempotency_key_idx
  on public.payments (idempotency_key)
  where idempotency_key is not null;
